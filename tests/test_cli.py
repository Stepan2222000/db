def test_generate_compose_internal_api_smoke(tmp_path: Path) -> None:
    services_dir = tmp_path / "services"
    services_dir.mkdir()
    (tmp_path / ".env").write_text(
        "DB_POSTGRES_VERSION=18\n"
        "DB_DEFAULT_POSTGRES_PASSWORD=secret\n",
        encoding="utf-8",
    )
    (services_dir / ".env.test").write_text(
        "POSTGRES_USER=admin\n"
        "POSTGRES_PASSWORD=pa$word\n"
        "POSTGRES_PORT=5401\n",
        encoding="utf-8",
    )

    compose_path = generate_compose(tmp_path)

    assert compose_path.exists()
    assert compose_path.read_text(encoding="utf-8").startswith(
        f"{AUTOGENERATED_COMMENT}\n"
    )


@pytest.mark.parametrize(
    ("argv", "cwd"),
    (
        ([sys.executable, "-m", "ops.cli", "--help"], REPO_ROOT),
        ([sys.executable, "ops/cli.py", "--help"], REPO_ROOT),
    ),
)
def test_help_works_for_module_and_script_entrypoints(
    argv: list[str],
    cwd: Path,
) -> None:
    result = subprocess.run(
        argv,
        cwd=cwd,
        capture_output=True,
        text=True,
        check=False,
    )

    assert result.returncode == 0, result.stderr
    assert "Usage" in result.stdout
    for command_name in PUBLIC_COMMANDS:
        assert command_name in result.stdout
    assert "generate-compose" not in result.stdout


@pytest.mark.parametrize("command_name", STUB_COMMANDS)
def test_stub_commands_are_silent_success(command_name: str) -> None:
    result = subprocess.run(
        [sys.executable, "-m", "ops.cli", command_name],
        cwd=REPO_ROOT,
        capture_output=True,
        text=True,
        check=False,
    )

    assert result.returncode == 0
    assert result.stdout == ""
    assert result.stderr == ""


