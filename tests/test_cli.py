def test_add_confirm_runs_compose_and_wait(
    monkeypatch: pytest.MonkeyPatch,
    tmp_path: Path,
) -> None:
    services_dir = tmp_path / "services"
    services_dir.mkdir()
    (tmp_path / ".env").write_text(
        "DB_POSTGRES_VERSION=18\n"
        "DB_DEFAULT_POSTGRES_PASSWORD=secret\n",
        encoding="utf-8",
    )

    compose_calls: list[tuple[Path, str | None]] = []
    wait_calls: list[tuple[str, str, str]] = []

    monkeypatch.chdir(tmp_path)
    monkeypatch.setattr(add_command.typer, "confirm", lambda *args, **kwargs: True)
    monkeypatch.setattr(
        add_command,
        "compose_up",
        lambda project_root, service=None: compose_calls.append((project_root, service)),
    )
    monkeypatch.setattr(
        add_command,
        "wait_for_pg_ready",
        lambda container_name, service_name, postgres_user, **kwargs: wait_calls.append(
            (container_name, service_name, postgres_user)
        ),
    )

    add_command.add("demo", port=None, password="pw1")

    assert compose_calls == [(tmp_path, "demo")]
    assert wait_calls == [("demo", "demo", "admin")]


