def test_sizes_reports_running_and_stopped_services(
    monkeypatch: pytest.MonkeyPatch,
    caplog: pytest.LogCaptureFixture,
    tmp_path: Path,
) -> None:
    configs = [
        ServiceConfig(
            name="running_demo",
            env_path=tmp_path / "services" / ".env.running_demo",
            postgres_user="admin",
            postgres_password="secret",
            postgres_port=5401,
        ),
        ServiceConfig(
            name="stopped_demo",
            env_path=tmp_path / "services" / ".env.stopped_demo",
            postgres_user="admin",
            postgres_password="secret",
            postgres_port=5402,
        ),
    ]

    monkeypatch.chdir(tmp_path)
    monkeypatch.setattr(sizes_command, "load_all_service_configs", lambda project_root: configs)
    monkeypatch.setattr(
        sizes_command,
        "container_exists",
        lambda name: True,
    )
    monkeypatch.setattr(
        sizes_command,
        "container_is_running",
        lambda name: name == "running_demo",
    )
    monkeypatch.setattr(
        sizes_command,
        "query_database_size",
        lambda container_name, service_config: 7861951,
    )

    with caplog.at_level("INFO"):
        sizes_command.sizes()

    assert "running_demo: 0.01 GB" in caplog.text
    assert "stopped_demo: stopped, start the service first" in caplog.text


