#!/bin/bash
# Вставляет инструкцию "как читать" в начало YAML-файла.
# Использование: bash add-header.sh <путь-к-yaml>

set -euo pipefail

FILE="${1:?Укажи путь к YAML-файлу}"

if [ ! -f "$FILE" ]; then
  echo "Файл не найден: $FILE" >&2
  exit 1
fi

if head -1 "$FILE" | grep -q '# ═'; then
  echo "Заголовок уже есть, пропускаю."
  exit 0
fi

HEADER='# ══════════════════════════════════════════════════════════
# Как читать этот документ
#
# YAML описывает логику системы на трёх уровнях:
#
# 1. purpose — общее описание: что система делает, для кого,
#    ключевые принципы. Читай первым.
#
# 2. definitions — справочные карточки технических сущностей.
#    [[id]] в любом тексте = ссылка на definition с таким id.
#    Здесь описано КАК устроены инструменты, файлы, конфиги.
#
# 3. modules → steps → links — граф flow. Модули группируют
#    шаги, стрелки (links) показывают переходы и ветвления.
#    Здесь описано ЧТО происходит в системе.
#
# Для реализации: шаги говорят что делать, definitions —
# как именно (API, форматы, параметры).
# ══════════════════════════════════════════════════════════
'

TEMP=$(mktemp)
echo "$HEADER" > "$TEMP"
cat "$FILE" >> "$TEMP"
mv "$TEMP" "$FILE"
echo "Заголовок добавлен в $FILE"
